//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// abstract: Start a thread
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CVbaMt::Start()
{
  if ( !m_fStarted )
  {
    ResumeThread( m_hThread );
    m_fStarted = TRUE;
  }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// abstract: Stop a thread
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CVbaMt::Stop()
{
  if ( m_fStarted )
  {
    SuspendThread(m_hThread);
    m_fStarted = FALSE;
  }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// abstract: Destroy a thread
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
HANDLE CVbaMt::Destroy()
{
  HANDLE hThread = m_hThread;
  //need to make sure the thread is running to shut down properly.
  while((int)ResumeThread(m_hThread) > 1);

  PostThreadMessage( m_dwThreadID, WM_QUIT, 0, 0 );


 
  delete this;

  return hThread;
}

